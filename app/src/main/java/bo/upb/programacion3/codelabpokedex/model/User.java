package bo.upb.programacion3.codelabpokedex.model;

public class User {
    private String user;
    private String password;
    private int image;

    public int getImage() {
        return image;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword() {
        this.password = password;
    }
}
